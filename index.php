  <h3 style="text-align: justify;">Principais Atividades</h3>
  <p style="text-align: justify;">As <strong>consultas</strong> têm como finalidade a apresentação de sugestões para análise de dados ou para planejamento de estudos que envolvem análises estatísticas, além de servirem para triagem de projetos a serem desenvolvidos pelo CEA. Em geral, têm duração de 1:30h e as sugestões para planejamento e/ou análise são apresentadas por escrito, caso seja possível identificar o problema estatístico (temos atendido muitos casos em que o próprio problema que é objeto de estudo não está bem definido e, nessa situação, a orientação estatística não é factível).</p>
  <p style="text-align: justify;">Participam da consulta: pesquisador(es) interessado(s) e orientador(a) (nos casos de mestrado ou doutorado), professores do departamento e alunos do bacharelado em Estatística.</p>
  <p>Os <strong>projetos</strong> desenvolvidos pelo CEA envolvem um completo planejamento e/ou análise estatística de dados. Em geral, o prazo para a realização de um projeto é de 4 meses (março a junho ou agosto a novembro). O produto desse tipo de projeto é um relatório contendo uma descrição detalhada das análises e suas conclusões.</p>
  <p style="text-align: justify;">Para que um projeto seja desenvolvido pelo CEA é necessário que ele seja submetido até janeiro (para trabalhos a serem analisados no 1o semestre) ou junho (para trabalhos a serem analisados no 2o semestre) a fim de que participe da triagem (usualmente realizada em fevereiro ou julho), em que será cotejado com outros projetos inscritos. Nessa triagem selecionam-se os projetos que serão analisados no semestre letivo subsequente. Cada projeto tem como consultores os professores do departamento e, como colaboradores, os alunos formandos do bacharelado em Estatística, sempre assessorados pelos pesquisadores interessados. A seleção dos projetos é baseada na quantidade e disponibilidade de dados, de consultores e alunos, etc. Projetos para os quais tenha sido realizada consulta anterior têm prioridade.</p>


