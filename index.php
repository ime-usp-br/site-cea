  <h3 style="text-align: justify;">Principais Atividades</h3>
  <p style="text-align: justify;">As <strong>consultas</strong> têm como finalidade a apresentação de sugestões para análise de dados ou para planejamento de estudos que envolvem análises estatísticas. São realizadas às terças-feiras, às 16 horas, durante os dois semestres letivos, e têm duração média de 1:30h. As inscrições podem ser realizadas a qualquer momento. As sugestões para
planejamento e/ou análise são apresentadas por escrito, caso seja possível identificar o problema estatístico (temos atendido muitos casos em que o próprio problema que é
objeto de estudo não está bem definido e, nessa situação, a orientação estatística não é factível).<strong> Ressaltamos que as atividades de consulta são pontuais, sem que haja a realização de uma análise estatística completa, o que é feito na modalidade projeto descrita a seguir.</strong></p>
  <p style="text-align: justify;">Participam da consulta: pesquisador(es) interessado(s) e orientador(a) (nos casos de mestrado ou doutorado), professores do departamento e alunos do bacharelado em Estatística.</p>
  <p>Os <strong>projetos</strong> desenvolvidos pelo CEA envolvem um completo planejamento e/ou análise estatística de dados. Em geral, o prazo para a realização de um projeto é de 4 meses (março a junho ou agosto a novembro). O produto desse tipo de projeto é um relatório contendo uma descrição detalhada das análises e suas conclusões.</p>
  <p style="text-align: justify;">Para que um projeto seja desenvolvido pelo CEA é necessário que ele seja submetido em janeiro/fevereiro (para trabalhos a serem analisados no 1º semestre) ou em
junho/julho (para trabalhos a serem analisados no 2º semestre) a fim de que participe da triagem (usualmente realizada em fevereiro/março ou julho/agosto), em que será
cotejado com outros projetos inscritos. Nessa triagem selecionam-se os projetos que serão analisados no semestre letivo subsequente. Cada projeto tem como consultores os professores do departamento e, como colaboradores, os alunos formandos do bacharelado em Estatística, sempre assessorados pelos pesquisadores interessados. A seleção dos projetos é baseada na quantidade e disponibilidade de dados, de consultores e alunos, etc. Projetos para os quais tenha sido realizada consulta anterior têm prioridade.</p>
  <p><strong>Uma vez que nos projetos realizados pelo CEA é feita uma análise estatística completa, usualmente solicita-se dos(as) pesquisadores(as) disponibilidade para a realização de várias reuniões com a equipe do CEA para o desenvolvimento adequado da análise durante o semestre.</strong></p>

